# Написати програму, яка буде зчитувати числа поки не зустріне від’ємне число.
#  При появі від’ємного числа програма зупиняється (якщо зустрічається 0 програма теж зупиняється).
while True:
    num = int(input('Input next number: '))
    if num > 0:
        print(num)
    else:
        print('break')
        break    



